def interpret(diff):
    """
    音声特徴量の差分から,状態変化を人が理解できる文章に変換する関数.

    この関数は異常検知を自動で断定するものではなく,
    通常状態と比較して「どのような傾向の変化が見られるか」を
    解釈コメントとして提示することを目的としている.

    Parameters
    ----------
    diff : dict
        通常音と比較音の特徴量差分を格納した辞書.
        各値は「比較音 - 通常音」で計算されていることを前提とする.
        例:
        {
            "rms":        音量（エネルギー）の差分,
            "centroid":   スペクトル重心の差分,
            "hf_ratio":   高周波成分比率の差分
        }

    Returns
    -------
    comments : list[str]
        音の変化に関する解釈文のリスト.
    """

    # 解釈コメントを格納するリスト
    comments = []

    # === 音量（RMS）の変化評価 ===
    # RMS が一定以上増加している場合,
    # 全体的な音エネルギーが増えていることを意味する.
    # 負荷の増加,接触の増大,稼働状態の変化などが考えられる.
    if diff["rms"] > 0.05:
        comments.append(
            "全体的な音量が増加しており,負荷や接触の増加が考えられます."
        )

    # === スペクトル重心の変化評価 ===
    # スペクトル重心が高周波側へ移動している場合,
    # 音質がシャープになっており,
    # 摩耗や表面状態の変化を示唆する可能性がある.
    if diff["centroid"] > 100:
        comments.append(
            "音の重心が高周波側に移動しており,摩耗や表面状態の変化が示唆されます."
        )

    # === 高周波成分比率の変化評価 ===
    # 高周波成分の割合が増えている場合,
    # 微小な異音,振動,ざらつきなどが
    # 発生し始めている兆候と考えられる.
    if diff["hf_ratio"] > 0.1:
        comments.append(
            "高周波成分の比率が増加しており,微小な異音が発生している兆候と考えられます."
        )

    # === 顕著な変化が検出されなかった場合 ===
    # どの指標も閾値を超えなければ,
    # 構造的な変化は小さいと解釈する.
    # 過剰な異常判定を防ぐための安全設計.
    if not comments:
        comments.append(
            "通常状態と比較して,顕著な構造変化は見られません."
        )

    return comments
